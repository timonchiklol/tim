def random_location():
    global l
    l = randint(1, 3)
    if l == 1:
        clear()
        bgcolor("black")
        setposition(0, 0)
        setheading(0)
        location()
        color("magenta4")
    if l == 2:
        clear()
        bgcolor("lightskyblue")
        setposition(0, 0)
        setheading(0)
        location2()
        color("magenta4")
    if l == 3:
        clear()
        bgcolor("gold4")
        setposition(0, 0)
        setheading(0)
        location3()
        color("magenta4")


